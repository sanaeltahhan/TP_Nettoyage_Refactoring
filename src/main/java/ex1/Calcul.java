package ex1;

/**
 * Classe qui réalise des opérations arythmétiques
 * @author DIGINAMIC
 */
public class Calcul {

	/** Méthode qui retourne l'addition des entiers a et b 
	 * 
	 * @param a int
	 * @param b int
	 * @return int resultat de l'addition
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/** Méthode qui retourne la soustraction des entiers a et b
	 * @param a int
	 * @param b int
	 * @return int résultat de la soustraction
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
